// sentencias if -> boolean -> true/false

// operadores condicionales

/* 
> mayor que (no es inclusivo)
< menor que (no es inclusivo)
>= mayor o igual que (es inclusivo)
<= menor o igual que (es inclusivo)
== igual que
*/
const edad = 18;
const esMayorDeEdad = edad >= 18;

if (!esMayorDeEdad) {
    console.log("Es verdadero");
} else {
    console.log("Es falso");
}