// Operadores de asignación

// Asignación (=)
const x = 5;
console.log("x: ", x);

// Asignación de suma (+=)
let sumaTotal = 0;
sumaTotal += x;
console.log("Suma total: ", sumaTotal);

// Asignación de resta (-=)
let restaTotal = 0;
restaTotal -= x;
console.log("Resta total: ", restaTotal);

// Asignación de multiplicación (*=)
let multiplicacionTotal = 20;
multiplicacionTotal *= x;
console.log("Multiplicación total: ", multiplicacionTotal);

// Asignación de división (/=)
let divisionTotal = 20;
divisionTotal /= x;
console.log("División total: ", divisionTotal);