// boolean - true o false
const True = true;
const False = false;

console.log("True: ", True);
console.log("False: ", False);

// number - positivos, negativos, decimales
const Positivo = 10_000;
const Negativo = -1;
const Decimal = 1.5;

console.log("Positivo: ", Positivo);
console.log("Negativo: ", Negativo);
console.log("Decimal: ", Decimal);

// string - "texto", 'texto', `texto`
const ComillasDobles = "texto";
const ComillasSimples = 'texto';
const ComillasInvertidas = `texto`;

console.log("ComillasDobles: ", ComillasDobles);
console.log("ComillasSimples: ", ComillasSimples);
console.log("ComillasInvertidas: ", ComillasInvertidas);

// undefined - sin definir
const Undefined = undefined;

console.log("Undefined: ", Undefined);

// null - ausencia de un valor
const Null = null;

console.log("Null: ", Null);